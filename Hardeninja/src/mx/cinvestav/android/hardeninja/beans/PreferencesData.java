package mx.cinvestav.android.hardeninja.beans;

public class PreferencesData {

	private String smsPhone;
	private String smsOnCode;
	private String smsOffCode;
	
	public String getSmsPhone() {
		return smsPhone;
	}

	public void setSmsPhone(String smsPhone) {
		this.smsPhone = smsPhone;
	}

	public String getSmsOnCode() {
		return smsOnCode;
	}

	public void setSmsOnCode(String smsOnCode) {
		this.smsOnCode = smsOnCode;
	}

	public String getSmsOffCode() {
		return smsOffCode;
	}

	public void setSmsOffCode(String smsOffCode) {
		this.smsOffCode = smsOffCode;
	}
	
	
	
}
